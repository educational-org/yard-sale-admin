import { ChevronLeftIcon, ChevronRightIcon } from '@heroicons/react/20/solid';


const Pagination = ({ pagination, setPagination,PRODUCT_OFFSET }: any) => {

    const paginationHandler = (action: string) => {
        if (pagination>=0 && action == "increment") {
            setPagination(pagination + PRODUCT_OFFSET)
        } else if (pagination>0  && action == "decrement") {
            setPagination(pagination - PRODUCT_OFFSET)
        }
    }
    return (
        <div className="flex items-center justify-between border-t border-gray-200 bg-white px-4 py-3 sm:px-6">
            <div className="flex flex-1 justify-between sm:hidden">
                <button onClick={() => paginationHandler("decrement")} className="pagination-mobile-btns relative inline-flex items-center rounded-md border border-gray-300 bg-white px-4 py-2 text-sm font-medium text-gray-700 hover:bg-gray-50">
                    Previous
                </button>
                <button  onClick={() => paginationHandler("increment")} className="pagination-mobile-btns relative ml-3 inline-flex items-center rounded-md border border-gray-300 bg-white px-4 py-2 text-sm font-medium text-gray-700 hover:bg-gray-50">
                    Next
                </button>
            </div>
            <div className="hidden sm:flex sm:flex-1 sm:items-center sm:justify-between">
                <div>
                    <p className="text-sm text-gray-700">
                        Pagination
                    </p>
                </div>
                <div>
                    <nav className="isolate inline-flex -space-x-px rounded-md shadow-sm" aria-label="Pagination">
                        <a onClick={() => paginationHandler("decrement")} href="#" className="relative inline-flex items-center rounded-l-md border border-gray-300 bg-white px-2 py-2 text-sm font-medium text-gray-500 hover:bg-gray-50 focus:z-20">
                            <span className="sr-only">Previous</span>
                            <ChevronLeftIcon className="h-5 w-5" aria-hidden="true" />
                        </a>
                        
                        <a onClick={() => paginationHandler("increment")} href="#" className="relative inline-flex items-center rounded-r-md border border-gray-300 bg-white px-2 py-2 text-sm font-medium text-gray-500 hover:bg-gray-50 focus:z-20">
                            <span className="sr-only">Next</span>
                            <ChevronRightIcon className="h-5 w-5" aria-hidden="true" />
                        </a>
                    </nav>
                </div>
            </div>
        </div>
    );
}
export default Pagination;